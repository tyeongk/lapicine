#include <unistd.h>

int	rev_print(int	ac, char **av)
{
	int	i;

	i = 0;
	if(av[1][i] != '/0')
		write(1, "\n", 1);
	if(ac == 2)
	{
		while (i > 0)
		{
			write(1, av[1][i - 1], 1);
			i--;
		}
		write(1, "\n", 1);
		return (0);

	}
	write(1, "\n", 1);
}
